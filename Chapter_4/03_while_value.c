#include<stdio.h>

int main(){
    int i = 0;
    while(i<=10)
    {
        printf("The value of i is :- %d\n",i);
        i++;
    }
    return 0;
}


/* 
If we write while(i<10){} them we will get the output from 0 to 9
eg:- while(i<10)
The value of i is :- 0 to The value of i is :- 9


If we  write while(i<=10){} then we will get the output from 0 to 10
eg:- while(i<=10)
The value of i is :- 0 to The value of i is :- 10
*/
